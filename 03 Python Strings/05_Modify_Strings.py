# ‚≠ê Note: All string methods returns new values. They do not change the original string.

a = "Hello, World!"

 #! To UpperCase
print(a.upper())

 #! To LowerCase
print(a.lower())

#! The strip() method removes any whitespace from the beginning or the end
b = "    Hello, World!  "
print(b.strip()) 

#! Replace String
print(a.replace('H', 'J'))

#! Split String
print(a.split(","))
